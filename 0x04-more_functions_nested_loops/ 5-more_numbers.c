#include "main.h"

/**
 * more_numbers - entry point
 *
 * Description: Prints the numbers with _putchar
 *
 * Return: void
 */

void more_numbers(void)
{
    int i , z;
    for (i = 0; i < 15; i++)
    {
        for (z = 0; z < 15; z++)
        {
            _putchar('z');
        }
        _putchar('\n');
    }
    
}
